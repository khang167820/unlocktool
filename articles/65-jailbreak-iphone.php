            <h2 id="jailbreak-la-gi"><i class="fas fa-question-circle me-2"></i>1. Jailbreak iPhone Là Gì? Lợi Ích Và Rủi Ro</h2>
            
            <p><strong>Jailbreak</strong> là quá trình khai thác lỗ hổng bảo mật (exploit) trong hệ điều hành iOS để có được quyền truy cập root (quyền cao nhất) vào hệ thống. Khi jailbreak, bạn có thể vượt qua các hạn chế mà Apple đặt ra và tùy biến iPhone theo ý muốn.</p>

            <h3>Lợi ích của Jailbreak</h3>
            <ul>
                <li><strong>Cài đặt tweak:</strong> Thay đổi giao diện, thêm tính năng mà Apple không có</li>
                <li><strong>Cài app bên ngoài App Store:</strong> Cài IPA, app bị Apple từ chối</li>
                <li><strong>Bypass iCloud:</strong> Bắt buộc phải jailbreak để bypass Activation Lock trên thiết bị checkm8</li>
                <li><strong>Tùy biến sâu:</strong> Theme icon, font, animation, control center...</li>
                <li><strong>Truy cập file system:</strong> Quản lý file như trên máy tính</li>
                <li><strong>Tải nhạc/video miễn phí:</strong> Các tweak như Cercube, uYou+...</li>
                <li><strong>Chặn quảng cáo:</strong> System-wide ad blocking</li>
            </ul>

            <h3>Rủi ro của Jailbreak</h3>
            <div class="warning-box">
                <ul class="mb-0">
                    <li><strong>Mất bảo hành:</strong> Apple có thể từ chối bảo hành (có thể restore để xóa dấu vết)</li>
                    <li><strong>Bảo mật giảm:</strong> Tweak độc hại có thể ăn cắp dữ liệu</li>
                    <li><strong>Mất ổn định:</strong> Một số tweak conflict gây crash, respring loop</li>
                    <li><strong>Không OTA update:</strong> Update iOS OTA có thể gây bootloop</li>
                    <li><strong>App banking detect:</strong> Một số app banking, game không cho dùng trên máy jailbreak</li>
                </ul>
            </div>

