                <h2><i class="fas fa-barcode text-primary me-2"></i>1. IMEI Là Gì?</h2>
                
                <p><strong>IMEI</strong> (International Mobile Equipment Identity) là số nhận dạng thiết bị di động quốc tế, gồm <strong>15 chữ số</strong>, duy nhất cho mỗi thiết bị di động trên thế giới.</p>

                <div class="info-box">
                    <h5><i class="fas fa-info-circle me-2"></i>Ví dụ IMEI</h5>
                    <p class="mb-0"><code style="font-size: 1.3rem;">35 123456 789012 3</code></p>
                    <p class="mb-0 mt-2 small">Cấu trúc: TAC (8 số) + SNR (6 số) + Check digit (1 số)</p>
                </div>

                <h3>1.1. Tại sao IMEI quan trọng?</h3>
                <ul>
                    <li><i class="fas fa-check text-success me-2"></i><strong>Định danh duy nhất:</strong> Mỗi thiết bị có 1 IMEI riêng</li>
                    <li><i class="fas fa-check text-success me-2"></i><strong>Theo dõi nguồn gốc:</strong> Biết máy sản xuất ở đâu, khi nào</li>
                    <li><i class="fas fa-check text-success me-2"></i><strong>Kiểm tra bảo hành:</strong> Xác minh tình trạng warranty</li>
                    <li><i class="fas fa-check text-success me-2"></i><strong>Phát hiện máy cấm:</strong> Blacklist, báo mất, nợ cước</li>
                    <li><i class="fas fa-check text-success me-2"></i><strong>Kiểm tra khóa:</strong> iCloud, Knox, FRP, Carrier lock</li>
                </ul>

                <h3>1.2. IMEI vs Serial Number</h3>
                <table class="table table-bordered">
                    <thead class="table-primary">
                        <tr>
                            <th>IMEI</th>
                            <th>Serial Number</th>
                        </tr>
                    </thead>
                    <tbody>
                        <tr>
                            <td>15 chữ số</td>
                            <td>Chữ + số (12-17 ký tự)</td>
                        </tr>
                        <tr>
                            <td>Dùng cho mạng di động</td>
                            <td>Dùng cho nhận dạng chung</td>
                        </tr>
                        <tr>
                            <td>Có thể có 2 IMEI (dual SIM)</td>
                            <td>Chỉ có 1 Serial</td>
                        </tr>
                        <tr>
                            <td>Quay bằng *#06#</td>
                            <td>Xem trong Settings</td>
                        </tr>
                    </tbody>
                </table>
            </section>

