                        <li>Đăng ký sử dụng mạng di động (gọi, SMS, 4G/5G)</li>
                        <li>Theo dõi nếu máy bị mất cắp</li>
                        <li>Kiểm tra thông tin bảo hành</li>
                    </ul>
                </div>

