                <h2><i class="fas fa-info-circle text-primary me-2"></i>1. IMEI Là Gì? Tại Sao Bị Null?</h2>
                
                <p><strong>IMEI</strong> (International Mobile Equipment Identity) là số nhận dạng duy nhất của mỗi điện thoại di động. IMEI gồm 15 chữ số, được lưu trong vùng nhớ EFS (Encrypted File System) của điện thoại.</p>

