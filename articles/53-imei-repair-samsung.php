                <p><strong>IMEI null</strong> hoặc <strong>invalid</strong> là tình trạng IMEI bị mất hoặc hỏng trong vùng nhớ EFS. Khi quay *#06#, máy sẽ hiển thị:</p>
                <ul>
                    <li>000000000000000</li>
                    <li>IMEI: null</li>
                    <li>IMEI: Invalid</li>
                    <li>Không hiện gì cả</li>
                </ul>
